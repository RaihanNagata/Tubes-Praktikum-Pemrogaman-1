package tubes;

public class User {
	private String username;
	private StrukturStack stack;
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public StrukturStack getStack() {
		return stack;
	}
	public void setStack(StrukturStack stack) {
		this.stack = stack;
	}
	
	
}
